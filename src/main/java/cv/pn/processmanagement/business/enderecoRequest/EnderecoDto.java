package cv.pn.processmanagement.business.enderecoRequest;

public class EnderecoDto {

    private String ilha;
    private String concelho;
    private String freguesia;
    private String zona;
    private String localidade;
    private Integer idIlha;
    private Integer idConcelho;
    private Integer idFreguesia;
    private Integer idZona;
    private Integer idLocalidade;

    public String getIlha() {
        return ilha;
    }

    public void setIlha(String ilha) {
        this.ilha = ilha;
    }

    public String getConcelho() {
        return concelho;
    }

    public void setConcelho(String concelho) {
        this.concelho = concelho;
    }

    public String getFreguesia() {
        return freguesia;
    }

    public void setFreguesia(String freguesia) {
        this.freguesia = freguesia;
    }

    public String getZona() {
        return zona;
    }

    public void setZona(String zona) {
        this.zona = zona;
    }

    public String getLocalidade() {
        return localidade;
    }

    public void setLocalidade(String localidade) {
        this.localidade = localidade;
    }

    public Integer getIdIlha() {
        return idIlha;
    }

    public void setIdIlha(Integer idIlha) {
        this.idIlha = idIlha;
    }

    public Integer getIdConcelho() {
        return idConcelho;
    }

    public void setIdConcelho(Integer idConcelho) {
        this.idConcelho = idConcelho;
    }

    public Integer getIdFreguesia() {
        return idFreguesia;
    }

    public void setIdFreguesia(Integer idFreguesia) {
        this.idFreguesia = idFreguesia;
    }

    public Integer getIdZona() {
        return idZona;
    }

    public void setIdZona(Integer idZona) {
        this.idZona = idZona;
    }

    public Integer getIdLocalidade() {
        return idLocalidade;
    }

    public void setIdLocalidade(Integer idLocalidade) {
        this.idLocalidade = idLocalidade;
    }
}
